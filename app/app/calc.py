"""
Simple module for arithmetic operations.
"""


def add(x, y):
    """Adds two numbers."""
    return x + y


def sub(x, y):
    """Subtracts two numbers."""
    return x - y


def mul(x, y):
    """Multiplies two numbers."""
    return x * y


def div(x, y):
    """Divides two numbers and returns integer."""
    return x // y


def truediv(x, y):
    """Divides two numbers."""
    return x / y
